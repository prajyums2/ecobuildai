function formatIEEE(ref) {
    let citation = `[${ref.id}] ${ref.authors}`;
    
    if (ref.title) {
        citation += ref.title.includes('IS ') || ref.title.includes('ISO ') 
            ? `, "${ref.title}"`
            : `, "${ref.title},"`;
    }
    
    if (ref.journal) {
        citation += ` *${ref.journal}*`;
    }
    if (ref.volume) {
        citation += `, vol. ${ref.volume}`;
    }
    if (ref.issue) {
        citation += `, no. ${ref.issue}`;
    }
    if (ref.page) {
        citation += `, p. ${ref.page}`;
    }
    if (ref.pages) {
        citation += `, pp. ${ref.pages}`;
    }
    
    citation += `, ${ref.year}`;
    
    if (ref.doi) {
        citation += `. doi: ${ref.doi}`;
    }
    if (ref.publisher) {
        citation += `. ${ref.publisher}`;
    }
    
    return citation;
}

function getAllCitations() {
    const allRefs = [];
    
    Object.keys(CITATIONS).forEach(key => {
        const section = CITATIONS[key];
        section.references.forEach(ref => {
            allRefs.push({
                ...ref,
                category: section.code,
                formatted: formatIEEE(ref)
            });
        });
    });
    
    return allRefs;
}

function getCitationsByCategory(category) {
    const categoryMap = {
        'structural': ['is_875', 'is_1893', 'is_456', 'is_13920', 'is_10262'],
        'green_building': ['green_building'],
        'environmental': ['lca'],
        'materials': ['materials', 'ahp'],
        'bim': ['bim'],
        'cost': ['cost_estimation'],
        'quality': ['qc']
    };
    
    const keys = categoryMap[category?.toLowerCase()];
    if (!keys) {
        return null;
    }
    
    const results = {};
    keys.forEach(key => {
        if (CITATIONS[key]) {
            results[key] = {
                code: CITATIONS[key].code,
                title: CITATIONS[key].title,
                references: CITATIONS[key].references.map(ref => ({
                    ...ref,
                    formatted: formatIEEE(ref)
                }))
            };
        }
    });
    
    return results;
}

function getCitationsByCode(code) {
    const codeKey = code?.toLowerCase().replace(/-/g, '_');
    
    if (!CITATIONS[codeKey]) {
        return null;
    }
    
    const section = CITATIONS[codeKey];
    return {
        code: section.code,
        title: section.title,
        references: section.references.map(ref => ({
            ...ref,
            formatted: formatIEEE(ref)
        }))
    };
}

function getBibliography() {
    const bib = {};
    Object.keys(CITATIONS).forEach(key => {
        bib[CITATIONS[key].code] = CITATIONS[key].references.map(ref => formatIEEE(ref));
    });
    return bib;
}

function getCategories() {
    return Object.keys(CITATIONS).map(key => ({
        id: key,
        code: CITATIONS[key].code,
        title: CITATIONS[key].title,
        referenceCount: CITATIONS[key].references.length
    }));
}

